def make_text(text, x, y):
    graphics.set_font("bitmap6")
    graphics.set_pen(graphics.create_pen(255, 255, 255))
    graphics.text(text, x, y, -1, 1)


def print_char(letter,xpos,ypos,size=1):
    graphics.set_pen(graphics.create_pen(255, 255, 255))

    origin = xpos
    charval = ord(letter)
    #print(charval)
    index = charval-32 #start code, 32 or space
    #print(index)
    character = bitmapFont5x9[index] #this is our char...
    rows = [character[i:i+5] for i in range(0,len(character),5)]
    #print(rows)
    for row in rows:
        #print(row)
        for bit in row:
            #print(bit)
            if bit == '1':
                graphics.pixel(xpos,ypos)
                if size==2:
                    graphics.pixel(xpos,ypos+1)
                    graphics.pixel(xpos+1,ypos)
                    graphics.pixel(xpos+1,ypos+1)
            xpos+=size
        xpos=origin
        ypos+=size
#    display.update()

def print_string(string,xpos,ypos,size=1):
    if size == 2:
        spacing = 12
    else:
        spacing = 6
    for i in string:
        print_char(i,xpos,ypos,size)
        xpos+=spacing
    

